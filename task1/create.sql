-- CREATE TABLE for ORDER
CREATE TABLE ORDER (
    ORDER VARCHAR2(15) NOT NULL,
    "DATE" DATE NOT NULL,
    AMOUNT CHAR(5) NOT NULL,
    CSTM NUMBER(11,0),
    PRO NUMBER(11,0),
    CONSTRAINT UK_ORDER_DATE_CSTM_PRO UNIQUE (DATE, CSTM, PRO),
    CONSTRAINT FK_ORDER_CSTM FOREIGN KEY (CSTM) REFERENCES CUSTOMER(ID),
    CONSTRAINT FK_ORDER_PRO FOREIGN KEY (PRO) REFERENCES PRODUCTS(ID)
);

-- CREATE TABLE for CUSTOMERÃŸ
CREATE TABLE CUSTOMER (
    ID NUMBER(11,0) NOT NULL,
    NAME VARCHAR2(40),
    SURNAME VARCHAR2(40) NOT NULL,
    STR VARCHAR2(60),
    NO VARCHAR2(5),
    ZIP VARCHAR2(5) NOT NULL,
    PLACE VARCHAR2(50) NOT NULL,
    GEND CHAR(1),
    BDAY DATE,
    CONSTRAINT PK_CUSTOMER PRIMARY KEY (ID)
);

-- CREATE TABLE for PRODUCTS
CREATE TABLE PRODUCTS (
    ID NUMBER(11,0) NOT NULL,
    NAME VARCHAR2(50) NOT NULL,
    SORT VARCHAR2(20),
    SZ NUMBER(3,2) NOT NULL,
    PRICE NUMBER(3,2) NOT NULL,
    CONSTRAINT PK_PRODUCTS PRIMARY KEY (ID)
);



